"""Bridge between certification profiles and ACT4 test generation.

This script:
- maps a profile name (e.g. rvi20, mc100) to the extension list ACT4 expects, using the UDB auto-generated cfgs/profile/ YAMLs.
- checks that all mandatory extensions for the chosen profile are implemented by the core.

stdout: comma-separated extension list (passed to ACT4 via EXTENSIONS=)
stderr: diagnostic table showing mandatory check results

Exit codes: 0 = success, 1 = mandatory extensions missing, 2 = bad arguments
"""

import argparse
import os
import sys

import yaml

# ---------------------------------------------------------------------------
# profile registry — maps CLI names to YAML filenames under vendor_lib/riscv-arch-test/act4/external/riscv-unified-db/cfgs/profile
# source: auto-generated by `./do gen:cfg` in the UDB root.
# To add a profile, ensure it exists in cfgs/profile/ and add an entry here.
# ---------------------------------------------------------------------------
PROFILES = {
    "rvi20": "RVI20U32.yaml",
}

def parse_profile(path):
    """Return mandatory and non mandatory extensions from /cfgs/profile."""
    with open(path) as f:
        data = yaml.safe_load(f)
    mandatory = {e["name"] for e in data.get("mandatory_extensions", [])}
    optional = {e["name"] for e in data.get("non_mandatory_extensions", [])}
    return mandatory, optional

def parse_core(path):
    """Return extension names from core configuration."""
    with open(path) as f:
        data = yaml.safe_load(f)
    return {e["name"] for e in data.get("implemented_extensions", [])}

def sort_extensions(exts):
    single = sorted(e for e in exts if len(e) == 1)
    multi = sorted(e for e in exts if len(e) > 1)
    return single + multi

def fmt(exts):
    return ", ".join(sort_extensions(exts)) if exts else "(none)"

def main():
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--profile", required=True,
                        help=f"Certification profile ({', '.join(PROFILES)})")
    parser.add_argument("--core-yaml", required=True,
                        help="Path to the core's UDB YAML (implemented_extensions)")
    parser.add_argument("--udb-root", required=True,
                        help="Path to riscv-unified-db root")
    args = parser.parse_args()

    # validate profile
    if args.profile not in PROFILES:
        print(f"ERROR: unknown profile '{args.profile}'. "
              f"Supported: {', '.join(sorted(PROFILES))}", file=sys.stderr)
        sys.exit(2)

    # resolve paths
    prof_path = os.path.join(args.udb_root, "cfgs", "profile", PROFILES[args.profile])
    if not os.path.isfile(prof_path):
        print(f"ERROR: profile YAML not found: {prof_path}", file=sys.stderr)
        sys.exit(2)
    if not os.path.isfile(args.core_yaml):
        print(f"ERROR: core YAML not found: {args.core_yaml}", file=sys.stderr)
        sys.exit(2)

    # parse
    mandatory, optional = parse_profile(prof_path)
    core_exts = parse_core(args.core_yaml)

    # check mandatory requirements against core
    mand_present = mandatory & core_exts
    mand_missing = mandatory - core_exts

    all_profile = mandatory | optional # for what extensions ACT4 generetes test (full profile) 
    will_test = all_profile & core_exts # what extensions will act4 test
    will_skip = all_profile - core_exts # what extensions will act4 not test (from full profile scope)

    # diagnostic
    core_name = os.path.splitext(os.path.basename(args.core_yaml))[0]
    hdr = f"Profile '{args.profile}' vs core '{core_name}'"
    sep = "\u2500" * len(hdr)
    print(f"\n  {hdr}", file=sys.stderr)
    print(f"  {sep}", file=sys.stderr)
    print(f"  MANDATORY  present : {fmt(mand_present)}", file=sys.stderr)
    print(f"  MANDATORY  MISSING : {fmt(mand_missing)}", file=sys.stderr)
    print(f"  will test          : {fmt(will_test)}", file=sys.stderr)
    print(f"  will skip          : {fmt(will_skip)}", file=sys.stderr)
    print(f"  {sep}\n", file=sys.stderr)

    if mand_missing:
        print(f"ERROR: mandatory extensions not implemented by core: "
              f"{fmt(mand_missing)}", file=sys.stderr)
        sys.exit(1)

    # stdout: full profile extension list
    result = sort_extensions(all_profile)
    print(",".join(result))

if __name__ == "__main__":
    main()
